	What is Inner Wheel</a>
	</h2>
	<div class="art-PostContent">

	<p>Inner Wheel is one of internationally reputed Women&rsquo;s voluntary &ldquo;service&rdquo; Organization involved in philanthropic activities throughout the world. Inner Wheel Clubs throughout the world comprise the womenfolk of Rotarians. It is active in over 100 countries and geographic locations with over a hundred thousand members.</p>
	<p><img align="left" alt="" border="1" height="135" src="wp-content/uploads/InnerWheel.jpg" width="135" />Inner Wheel concentrates upon Friendship and Personal Service. It is not what is termed a money raising organization but each club has projects that build fellowship and raise money to support charities.</p>
	<p>One of the greatest features of Inner Wheel is the opportunity given to members of the world over to get know one another, thus contributing to International Friendship and Understanding. Although there is a serious side to this organization, the emphasis is upon members enjoying the part they play in the world of Inner Wheel.</p>
	<p>International Inner Wheel began in July, 1967. International Inner Wheel is one of the largest women&rsquo;s organizations with 98,173 members, spread over 103 countries in 168 Districts in almost 4130 clubs. The structure of Inner Wheel is :</p>
	<ul>
	<li>International Inner Wheel</li>
	<li>Associations &amp; Councils (Eligible countries)</li>
	<li>Non District Clubs</li>
	<li>Districts (Clubs from a District)</li>
	<li>Clubs (12 Members from a Club)</li>
	</ul>
	<p>As an NGO, International Inner Wheel has a consultative status at the UN and consequently members are appointed to represent Inner Wheel at the United States, Geneva and 2 in Vienna.<br />
	Since its inception in 1924, Inner Wheel continues to grow as new clubs from in different countries.</p>
	<p>Each Club enjoys close ties with its corresponding Rotary Club and is always willing to support and assist Rotary when invited to do so.</p>
	<p>Membership is open to :</p>
	<p>The categories of active membership which show our links with Rotary are as Follows :</p>
	<ul>
	<li>Wife of a Rotarian or former Rotarian.</li>
	<li>Female partner of a male Rotarians.</li>
	<li>Widow of a Rotarian or former Rotarian.</li>
	<li>Mother Of a Rotarian or former Rotarian.</li>
	<li>Sister of a Rotarian or former Rotarian.</li>
	<li>Daughter (over 18) of a Rotarian or former Rotarian.</li>
	<li>Step Daughter (over 18) of a Rotarian or former Rotarian.</li>
	<li>Daughter in law of a Rotarian or former Rotarian.</li>
	<li>Mother of an Inner Wheel Member</li>
	<li>Sister of an Inner Wheel Member</li>
	<li>Daughter of an Inner Wheel Member</li>
	<li>Female Rotarian or Former Female Rotarian</li>
	<li>Female former member of Rotaract</li>
	<li>Wife or Mother of a Female former member of Rotaract.</li>
	</ul>
	<p>A club may have a maximum of 4 honorary members ; these are women who have shown support for Inner Wheel but who do not fall into any of the above categories. Honorary members can not take office in the club, they are elected annually and may with the agreement of the members of the club, be offered full membership after two years.</p>
	<p>Membership in a club can be taken up by a foreign national having the same qualification.</p>
	<p>Meetings :</p>
	<p>Inner Wheel clubs are open to make their own program and projects. It is absolutely their choice.&nbsp; Meetings are held once in a month and take the form of business meeting, sometimes with a speaker. These meeting gives excellent opportunity for developing friendship and offering service.</p>
	<p>There are Standing rules, Bylaws and a constitution to follow.</p>
	<p>International Inner Wheel holds a convention, open to all members, every three years.</p>
	<p>Inner Wheel year is from 1st July to 30th June.</p>

