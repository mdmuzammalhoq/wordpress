														<p>Inner Wheel is one of internationally reputed Women&rsquo;s voluntary &ldquo;service&rdquo; Organization involved in philanthropic activities throughout the world. Inner Wheel Clubs throughout the world comprise the womenfolk of Rotarians. It is active in over 100 countries and geographic locations with over a hundred thousand members. Inner Wheel concentrates upon Friendship and Personal Service. It is not what is termed a money raising organization but each club has projects that build fellowship and raise money to support charities. One of the greatest features of Inner Wheel is the opportunity given to members of the world over to get know one another, thus contributing to International Friendship and Understanding. Although there is a serious side to this organization, the emphasis is upon members enjoying the part they play in the world of Inner Wheel. International Inner Wheel began in July, 1967. International Inner Wheel is one of the largest women&rsquo;s organizations with 98,173 members, spread over 103 countries in 168 Districts in almost 4130 clubs. The structure of Inner Wheel is : International Inner Wheel Associations &amp; Councils (Eligible countries) Non District Clubs Districts ($ Clubs from a District) Clubs (12 Members from a Club) As an NGO, International Inner Wheel has a consultative status at the UN and consequently members are appointed to represent Inner Wheel at the United States, Geneva and 2 in Vienna. Since its inception in 1924, Inner Wheel continues to grow as new clubs from in different countries. Each Club enjoys close ties with its corresponding Rotary Club and is always willing to support and assist Rotary when invited to do so. Membership is open to : The categories of active membership which show our links with Rotary are as Follows :</p>
