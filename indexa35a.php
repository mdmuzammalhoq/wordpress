														<h2 class="art-PostHeader">
														<a href="indexa35a.html?page_id=221" rel="bookmark" title="Permanent Link to Know Bangladeh">
														Know Bangladeh</a>
														</h2>
														<div class="art-PostContent">

														<p><strong>BANGLADESH: COUNTRY PROFILE</strong></p>
														<p><img align="left" alt="" height="203" src="wp-content/uploads/Rural_Bangladesh(1).jpg" width="234" />Bangladesh as a holiday making land exposes to many flamboyant facets. Its tourist attractions are many folded, which include archaeological sites, historical mosques and monuments, resorts, beaches, picnic spots, forests and tribal people, wildlife of various species. Bangladesh offers ample opportunities to tourists for angling, water skiing, river cruising, hiking, rowing, yachting, sea bathing as well as bringing one in close touch with pristine nature.</p>
														<p><strong><img align="right" alt="" height="156" src="wp-content/uploads/bangladesh_small_map.jpg" width="234" />LOCATION AND PHYSICAL FEATURES<br />
														</strong></p>
														<p>Located in the north-eastern part of South Asia, Bangladesh lies between 20&ordm;34&#39; and 26&ordm;36&#39; north latitude and 88&ordm;01&#39; and 92&ordm;41&#39; east longitude. The majestic Himalayas stand some distance to the north, while in the south lies the Bay of Bengal. The gangetic Plains of west Bengal border the country on the west and in the east lie the hilly and forested regions of Tripura , Mizoram(India) and Myanmar. These picturesque geographical boundaries frame a low lying plain of about 1,47,570 sq.km criss-crossed by innumerable rivers and streams. Mighty rivers the Padma(Ganges), the Brahamaputra(Jamuna) &amp; the Meghna and&nbsp; the Karnafuli. This is Bangladesh, a fertile land where nature is bounteous.</p>
														<p>Much of the country&#39;s land area has been built up from alluvial deposits brought down by the major rivers. The country is mostly flat except for a range of hills in the south-east. The topography of the country is characterized by wooded marshy lands and jungles with plane lands occupying most of the river basins. There are deep forested regions in Sylhet, Rangamati, Khagrachhari and Bandarban Hill Districts, Sundarbans (the World Heritage site), Mymensingh and Tangail.</p>
														<p><strong><object align="left" classid="clsid:d27cdb6e-ae6d-11cf-96b8-444553540000" codebase="http://download.macromedia.com/pub/shockwave/cabs/flash/swflash.cab#version=6,0,40,0"><param name="quality" value="high" /><param name="movie" value="wp-content/uploads/_Bangladesh.swf" /><embed pluginspage="http://www.macromedia.com/go/getflashplayer" quality="high" src="wp-content/uploads/_Bangladesh.swf" type="application/x-shockwave-flash"></embed></object>HISTORY OF EMERGENCE<br />
														</strong></p>
														<p>Bangladesh has a logn and eventful history as a nation. Although it enjoyed as a free and sovereign state only in 1971, after a nine month-long war of liberation, the land itself and its people, have their origin in antiquity. The earliest nation of Bangladesh in found in the 9th century BC Indian epic the Mahabharata. There are evidences of story Mongoloid presence at the time.Then in 5th &amp; 6th century BC came the Aryans from Central Asia and dravidians from western India. The Hindu and Buddhist dynasties of guptas, palas and senas ruled the country until 13th century, when muslim conquerers took over the reigns of the country.</p>
														<p>The muslim rules either belonging to independent dynasties such as the Hossain Shahi or Ilyas Shahi dynasties or Viceroys exercising power on behalf of the imperial seat of Delhi and continued to rule the country until the middle of the 18th century, when the British took over the control of Bengal and eventually the whole of India.The Europeans, mainly Portuguese, Dutch, French and British traders had began to arrive in Bangladesh from the 15th century and extended an economic control over the region.</p>
														<p><strong>Modern Period<br />
														</strong></p>
														<p>In 1757 British coloniers defeated the last Muslim ruler of Bengal, Nowab Sirajuddoullah at Palassey. After the end of the British rule in 1947, the country was partitioned into India and Pakistan. But the movement for autonomy for East Pakistan began within a couple of years because of linguistic and cultural differences and economic disparity between the two wings. The seeds of independence were sown through the great Language Movement of 1952 to recognise Bangla as a State Language.Then East Pakistan emerged as a sovereign and independent state of Bangladesh. After nine month-long sanguinary war of liberation. In which three million people courted matyrdom.</p>
														<p><u>Demography<br />
														</u></p>
														<p>Bangladesh has a population of 133.40 million making it the most densely polulated country of the world. 85% population live in rural areas. Density of population is about 900 per sq.km. There are about 2 million people of 23 tribes. They dwell mostly in Rangamati, Khagrachhari, Bandarban and in part of northern districts. The tribes have exotic distinct cultures of their own.</p>
														<p><u>Language<br />
														</u></p>
														<p>The state language and mother toung is Bangla. English is widely spoken and understood. Now a days some young people are learning other languages like French, German, Spanish, Japanese, Chinies etc. for professional purpose.</p>
														<p><u>Religion<br />
														</u></p>
														<p>Bangladesh is a land of religious freedom, harmony and tolerance. People of all castes and creeds live here in perfect harmony. The percentage distribution of population to religions is as follows : Muslims &#8211; 88%, Hindus &#8211; 10%, Christians, Buddhists and others 2%.</p>
														<p><u>Government<br />
														</u></p>
														<p>The country is officially known as The People&#39;s Republic of Bangladesh and has a parliamentary form of Government. The President is the Head of the State while the Prime Minister is the Head of the Government. The country is divided into 6(six) divisions namely Dhaka, Chittagong, Rajshahi, Barisal, Sylhet and Khulna. There are 64 districts, 465 upazillas (small administrative unit) and 85,650 villages under the six divisions.</p>
														<p><u>Legislature<br />
														</u></p>
														<p>Bangladesh has a 345-seat Parliament called &#39;Jatiyo Sangsad&#39;. 300 members of Jatiyo Sangsad are directly elected by the electoral college and 45 seats are reserved for women, nominated proportionately by the members of different parties in parliament. They are designated as Member of the Parliament.</p>
														<p><u>Economy<br />
														</u></p>
														<p>The Economy is charcterized by a large subsistence agricultural sector, which contributes to sum 85% of the country&#39;s population and small modern industrial sector. The total cultivable area is around 24 millons acres and there are a little more than 15 million cultivators. Major agricultural crops are rice, jute (the golden fiber), wheat, potato, pulses, sugarcane, tea, onion, garlic, ginger, tobacco etc. Tea, leather, ready made garments, frozen shrimp, jute and jute products are major foreign exchange earners. Export of hadicrafts is booming fast. Remittances from Bangladeshies employed abroad are also contributing significantly towards foreign exchange earnings.</p>
