														<h2 class="art-PostHeader">
														<a href="indexbc9e.html?page_id=37" rel="bookmark" title="Permanent Link to History">
														History</a>
														</h2>
														<div class="art-PostContent">

														<p>No historical account of Inner Wheel District 328 would be complete without some acknowledgement of how Inner Wheel began and developed in Bangladesh.</p>
														<p>The first Inner Wheel Club was formed in Chittagong in June 1979.</p>
														<p>The idea was conceived by Rotary Anne Sultana Nizam. She was undergoing treatment in London following a car accident. A group of Inner Wheel members having heard from their Rotary husbands about the horrific incident came to visit her with a bunch of beautiful flowers and gifts in the hospital. She was moved by this gesture and thought of forming a club in Chittagong. Her idea soon came to fruition. On her return to Chittagong in 1979 she initiated to form the first Inner Wheel Club in Chittagong.</p>
														<p>Soon after clubs sprang up in Dhaka, Khulna and one more in Chittagong named IWC of Agrabad. These clubs except Chittagong were part of District 329-belonging to the Association of Inner Wheel Clubs in India. IWC of Chittagong remained undistracted.</p>
														<p>In 1985 with a cluster of clubs District 328 was formed under the dynamic leadership of Prof. Roushanara Rahman. Mrs. Sumi Agrawal the then District Chairman of 329, with the Secretary Geeta Ghose and some members of Calcutta Midtown, Calcutta South East and Tally Gong came from West Bengal to Bangladesh. We had four clubs to start for the district. The Clubs were IWC of Agrabad, IWC of Dhaka, IWC of Khulna and IWC of Dhaka South. IWC of Chittagong joined after two months of the District formed. Thus was the beginning of the District and&#8212;&#8212;&#8211;the rest is history.</p>
														<p>First District Conference was held at Chittagong. IWC of Chittagong and IWC of Agrabad jointly hosted the conference on June 1986.</p>
														<p>The wealth of good wishes from many districts and countries offered much encouragement and goodwill towards the tremendous task of building an effective nationwide district and the knowledge that members would continue to make their contribution towards better understanding, service and friendships new and lasting have been formed and we have been able to learn at first-hand from our visitors from neighboring countries how much we have in common!</p>
														<p>Now there are total 25 Inner Wheel clubs and more than 500 members in District 328, Bangladesh.</p>

